package com.sys.po;

public class Work {
    private Integer work_id;
    private String work_name;
    private String work_money;
    private String work_grade;

    public Integer getWork_id() {
        return work_id;
    }

    public void setWork_id(Integer work_id) {
        this.work_id = work_id;
    }

    public String getWork_name() {
        return work_name;
    }

    public void setWork_name(String work_name) {
        this.work_name = work_name;
    }

    public String getWork_money() {
        return work_money;
    }

    public void setWork_money(String work_money) {
        this.work_money = work_money;
    }

    public String getWork_grade() {
        return work_grade;
    }

    public void setWork_grade(String work_grade) {
        this.work_grade = work_grade;
    }
}
